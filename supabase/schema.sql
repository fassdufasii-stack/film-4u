-- PROFILES: AI-Tuned User Preferences
CREATE TABLE IF NOT EXISTS public.profiles (
  id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
  full_name TEXT,
  preferred_language TEXT DEFAULT 'English',
  preferred_genres TEXT[] DEFAULT '{}',
  preferred_moods TEXT[] DEFAULT '{}', -- comfort, dark, nostalgic, hype
  sensitivity_toggles JSONB DEFAULT '{"violence": false, "sadness": false}', 
  mood_history JSONB DEFAULT '[]', -- History of mood searches & picks
  last_active TIMESTAMPTZ DEFAULT NOW(),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  avatar_url TEXT
);

-- MOVIES (OTT): Managed Catalog
CREATE TABLE IF NOT EXISTS public.movies_ott (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT,
  poster TEXT,
  banner TEXT,
  platform TEXT,
  release_date DATE,
  tags TEXT[],
  language TEXT,
  moods TEXT[],
  quality TEXT DEFAULT '4K',
  official_url TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- MOVIES (INDIE): User-Generated Content
CREATE TABLE IF NOT EXISTS public.movies_indie (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  creator_id UUID REFERENCES auth.users ON DELETE SET NULL,
  title TEXT NOT NULL,
  description TEXT,
  poster TEXT,
  banner TEXT,
  stream_url TEXT,
  tags TEXT[],
  language TEXT,
  moods TEXT[],
  is_verified BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- USER LIBRARY: Favorites & Watchlist Unified
CREATE TABLE IF NOT EXISTS public.user_library (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE NOT NULL,
  movie_id BIGINT NOT NULL,
  movie_type TEXT NOT NULL CHECK (movie_type IN ('ott', 'indie')),
  list_type TEXT NOT NULL CHECK (list_type IN ('favorite', 'watchlist')),
  added_at TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE(user_id, movie_id, movie_type, list_type)
);

-- WATCH HISTORY: Deep Behavioral Tracking
CREATE TABLE IF NOT EXISTS public.watch_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE,
  movie_id BIGINT,
  movie_type TEXT, 
  genre TEXT,
  language TEXT,
  mood_at_watch TEXT, -- Mood selected by user before watching
  time_of_day TEXT, -- 'morning', 'afternoon', 'evening', 'night'
  watched_at TIMESTAMPTZ DEFAULT NOW()
);

