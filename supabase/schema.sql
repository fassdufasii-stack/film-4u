-- USER QUOTAS: Daily Limits & Request Tracking
CREATE TABLE IF NOT EXISTS public.user_quotas (
  id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
  daily_ai_requests INTEGER DEFAULT 0,
  last_request_date DATE DEFAULT CURRENT_DATE,
  total_requests BIGINT DEFAULT 0,
  is_blocked BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE public.user_quotas ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can view own quota." ON user_quotas
  FOR SELECT USING (auth.uid() = id);

-- Trigger to create quota record for new users
CREATE OR REPLACE FUNCTION public.handle_new_user_quota()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.user_quotas (id) VALUES (new.id);
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_auth_user_created_quota
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user_quota();
