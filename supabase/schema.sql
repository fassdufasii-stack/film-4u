-- 3. STORAGE BUCKETS
-- ==========================================

-- Create buckets
INSERT INTO storage.buckets (id, name, public) 
VALUES ('posters', 'posters', true),
       ('videos', 'videos', true),
       ('avatars', 'avatars', true)
ON CONFLICT (id) DO NOTHING;

-- Storage Policies
CREATE POLICY "Public Access" ON storage.objects FOR SELECT USING ( bucket_id IN ('posters', 'videos', 'avatars') );
CREATE POLICY "Auth Upload" ON storage.objects FOR INSERT WITH CHECK ( bucket_id IN ('posters', 'videos', 'avatars') AND auth.role() = 'authenticated' );
CREATE POLICY "Owner Update" ON storage.objects FOR UPDATE USING ( bucket_id IN ('posters', 'videos', 'avatars') AND auth.uid() = owner );
CREATE POLICY "Owner Delete" ON storage.objects FOR DELETE USING ( bucket_id IN ('posters', 'videos', 'avatars') AND auth.uid() = owner );

